int	ft_iterative_factorial(int nb)
{
	int	i;
	int res;

	i = 1;
	res = 1;
	if (nb < 0 || nb > 16)
	{
		res = 0;
	}
	while (i < nb)
	{
		i++;
		res *= i;
	}
	return (res);
}

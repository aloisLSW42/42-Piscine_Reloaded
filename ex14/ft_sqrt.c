int	ft_sqrt(int nb)
{
	long	i;
	int		res;

	i = 2;
	res = 0;
	while (i * i < (long)nb)
	{
		i *= i;
	}
	i /= i;
	while (i * i <= (long)nb)
	{
		if (i * i == (long)nb)
		{
			res = i;
		}
		i++;
	}
	return (res);
}
